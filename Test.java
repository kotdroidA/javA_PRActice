import java.util.*;

class MathClassExample{
	

	public static void main(String[] args){
		
		//Math.abs()
		System.out.println(Math.abs(-6.34));	 


		//Math.round()
		System.out.println(Math.round(-6.34));
		System.out.println(Math.round(6.54));

		//Math.signum()
		System.out.println(Math.signum(-34));


		//Math.ceil()
		System.out.println(Math.ceil(45));
		System.out.println(Math.ceil(3.4));

		//Math.nextAfter
		System.out.println(Math.nextAfter(3,-7));
	 }
	
	
 }
